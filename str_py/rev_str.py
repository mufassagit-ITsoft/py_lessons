'''
Docstring for rev_str: 
This is an exercise in reversing a string. So "Hello" would have to
result as "olleH"
'''

str_py = "Star Trek"
rev_str = str_py[::-1]
print(rev_str)

str_py = "Star Trek"
rev_str = str_py[::-1].lower()
print(rev_str)
# lower() is used to convert all capital letters in a string to 
# uppercase.

str_py1 = "Star"
rev_str1 = str_py1[::-1]
print(rev_str1)

str_py2 = "Trek"
rev_str2 = str_py2[::-1]
print(rev_str2)